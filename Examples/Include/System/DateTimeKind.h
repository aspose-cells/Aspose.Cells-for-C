#ifndef _SYSTEM_DATETIMEKIND_H_
#define _SYSTEM_DATETIMEKIND_H_
namespace Aspose {
	namespace Cells {
		namespace System {
			enum DateTimeKind  {
				// Summary:
				//     The time represented is not specified as either local time or Coordinated
				//     Universal Time (UTC)->
				DateTimeKind_Unspecified = 0,
				//
				// Summary:
				//     The time represented is UTC->
				DateTimeKind_Utc = 1,
				//
				// Summary:
				//     The time represented is local time->
				DateTimeKind_Local = 2,
			};
		}
	}
}
#endif